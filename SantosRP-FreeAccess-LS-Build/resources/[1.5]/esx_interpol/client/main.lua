function cleanPlayer(playerPed)
	SetPedArmour(playerPed, 0)
	ClearPedBloodDamage(playerPed)
	ResetPedVisibleDamage(playerPed)
	ClearPedLastWeaponDamage(playerPed)
	ResetPedMovementClipset(playerPed, 0)
end

function setUniform(org, playerPed)
	TriggerEvent('skinchanger:getSkin', function(skin)
		if skin.sex == 0 then
			if Config.Uniforms[org].male then
				TriggerEvent('skinchanger:loadClothes', skin, Config.Uniforms[org].male)
			else
				ESX.ShowNotification(_U('no_outfit'))
			end

			if org == 'bullet_wear' then
				SetPedArmour(playerPed, 100)
			end
		else
			if Config.Uniforms[org].female then
				TriggerEvent('skinchanger:loadClothes', skin, Config.Uniforms[org].female)
			else
				ESX.ShowNotification(_U('no_outfit'))
			end

			if org == 'bullet_wear' then
				SetPedArmour(playerPed, 100)
			end
		end
	end)
end

function OpenCloakroomMenu()
	local playerPed = PlayerPedId()
	local grade = PlayerData.org.grade_name

	local elements = {
		{ label = _U('citizen_wear'), value = 'citizen_wear' },
		{ label = _U('bullet_wear'), value = 'bullet_wear' },
		{ label = _U('gilet_wear'), value = 'gilet_wear' }
	}

	if grade == 'recruit' then
		table.insert(elements, {label = _U('interpol_wear'), value = 'recruit_wear'})
	elseif grade == 'sergeant' then
		table.insert(elements, {label = _U('interpol_wear'), value = 'sergeant_wear'})
